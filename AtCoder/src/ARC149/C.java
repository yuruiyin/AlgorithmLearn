        private boolean[] getAllPrimes(int n) {
            boolean[] isNotPrime = new boolean[n + 1];
            for (int i = 2; i * i <= n; i++) {
                if (isNotPrime[i]) {
                    continue;
                }

                for (int j = i * i; j <= n; j += i) {
                    isNotPrime[j] = true;
                }
            }

            boolean[] prime = new boolean[n + 1];
            for (int i = 2; i <= n; i++) {
                if (!isNotPrime[i]) {
                    prime[i] = true;
                }
            }
            return prime;
        }

